<template>
  <label class="input-text">
    <input
      type="text"
      class="input-text-input"
      :placeholder="placeholder"
      v-model="localValue"
    >
  </label>
</template>

<script>
export default {
  props: {
    placeholder: String,
    value: String
  },
  data(){
    return {
      localValue: this.value
    }
  },
  watch: {
    localValue(val){
      this.$emit('input', val)
    }
  }
}
</script>

